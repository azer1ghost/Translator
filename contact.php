	            $.ajax({
	                  url: 'mail.php',
	                  type: 'POST',
	                  data: jQuery(curForm).serialize(),
	                  success: function(data) {
	                  var res=data.split("::");
	                  jQuery(curForm).find("div.formOverlay").remove();
	                  jQuery(curForm).prev('.expMessage').html(res[1]);
	                  if(res[0]=='Success')
	                  {
	                    jQuery(curForm).remove();
	                    jQuery(curForm).prev('.expMessage').html('');
	                  }
	                }
	            });
	            return false;
	        });
